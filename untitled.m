My goal is to discover instances of hysteresis at a given H0 point. 
My original simulation varied H0 the eight primary directions: 
E, W, N, S, NE, NW, SE, SW  

Inevitably, the solutions would move around hyteresis boundaries, opening 
the door to new shapes (sometimes many) for the same H0.

As an alternative approach, Does it make sense to take a known solution at 
a given H0 and systematically deform the physical variables of the vesicle 
(while leaving the area and volume the same). Then resolve the equations using 
the deformed vesicle to potentially "stumble" upon new shapes?